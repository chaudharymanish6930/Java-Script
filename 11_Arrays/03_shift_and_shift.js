myarr=[10,20,30,40,50]
console.log(myarr)

// unshift method is used to add element at the beginning of the array
myarr.unshift(5)
console.log(myarr)

// shift method is used to remove element from the beginning of the array
myarr.shift()
console.log(myarr)    //  [10,20,30,40,50]

myarr.shift()
console.log(myarr)   // [20,30,40,50]