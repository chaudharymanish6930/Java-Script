let myarr=[10,20,30,40,50]  
console.log(myarr)

// push() method
myarr.push(60)
myarr.push(70)  
console.log("After Push: ",myarr)    // [10,20,30,40,50,60,70]

// pop() method
myarr.pop()
console.log("After Pop: ",myarr)   // [10,20,30,40,50,60]
myarr.pop() 

console.log(myarr)    // [10,20,30,40,50]